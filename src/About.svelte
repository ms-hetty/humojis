    <h2>About <em>or</em> Why on earth did you create the Humojis project?</h2>
    <p>First, let me ask some questions back: What does it mean to you that 7.7 billion people currently live on our planet? Can you grasp this number? Do you know that only 2.5 billion people lived in 1950? Do you appreciate how vast the growth rates of the global population were in the last years?</p>

    <p>t's relatively easy to repeat these numbers and rates, but it's hard to understand them deeply. If we don't practice, our brains work on logarithmic scales: We are challenged to imagine that the number 1 billion is 1000 times one Millon. My goal is with this art project is to make it easier for us to imagine the world's population and its growth.</p>

    <p>Depending on the display of your phone or computer, one emoji converts roughly 3 to 40 million people. 3 Millon is still a big number, and it needs hard work to process these numbers. I want to invite you to translate these numbers into something you know, like a nearby city or a whole country. Then go on a journey and explorer the differences between regions, genders, age groups, and their change over time. Finally, be amazed at and proud of how we transformed. </p>

    <p>I've started the project with the idea to do it entirely by myself - from start to end. I've done a lot, but like we all, I don't live in an isolated bubble. It is one of my biggest learnings - not something new, but always good to be reminded of - to work together, ask for help, ask for feedback, and get ideas, appreciation, and support.</p>
    
